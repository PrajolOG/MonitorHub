    private void searchTFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchTFActionPerformed

        // Get the search text
        String TosearchText = searchTF.getText().trim().toLowerCase();

        // Clear any previous selection in the table
        stocksTBL.clearSelection();

        //Sort the monitorList using InsertionSort.java
        List<MonitorDetails> sortedList = new ArrayList<>(monitorList);
        
        InsertionSort insertionSort = new InsertionSort(); // Create an instance
        
        insertionSort.SortByModelName(sortedList); // Call the sort method

        //Perform Binary Search using BinarySearch.java
        int Search_result = BinarySearch.searchByModelName(sortedList, TosearchText);

        //Handle Search Results
        if (Search_result == -1) {
            // Not found
            JOptionPane.showMessageDialog(this, "The " + TosearchText + " model name was not found.", "Search Error", JOptionPane.ERROR_MESSAGE);
            updateTableSearch(new ArrayList<>()); // Clear the table
        } else {
            String targetModelName = sortedList.get(Search_result).getModelName().toLowerCase();

            // Create a list to store all matching monitors
            ArrayList<MonitorDetails> matchingMonitors = new ArrayList<>();
            for (MonitorDetails monitor : sortedList) {
                if (monitor.getModelName().toLowerCase().equals(targetModelName)) {
                    matchingMonitors.add(monitor);
                }
            }

            // Update the table with all matching monitors
            updateTableSearch(matchingMonitors);

            // Highlight all matching rows in the table
            for (int i = 0; i < matchingMonitors.size(); i++) {
                stocksTBL.addRowSelectionInterval(i, i);
            }
        }
    }//GEN-LAST:event_searchTFActionPerformed

