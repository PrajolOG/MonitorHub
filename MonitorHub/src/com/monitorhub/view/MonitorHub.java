    /**
     * Updates the `activityTextArea` with the current contents of the `activityStack`.
     * 
     * This method iterates through the `activityStack` in reverse order (most recent activity first),
     * appending each activity record to a single string, separated by newlines.
     * The resulting string is then used to update the text of the `activityTextArea`,
     * effectively displaying the activity log. The caret position is set to the beginning of the text area.
     *
     * 
     * @param activityStack A Stack (or similar structure) containing a log of user activities.
     * @param activityTextArea The JTextArea component used to display the activity log to the user.
     */
    
    private void updateActivityLog() {
        String log = ""; // Initialize an empty string
        Stack<String> activities = activityStack.getActivities();

        for (int i = activities.size() - 1; i >= 0; i--) {
            log += activities.get(i) + "\n\n";
        }

        activityTextArea.setText(log);
        activityTextArea.setCaretPosition(0); 
    }


    


    
    
    
    /**
     * Starts the loading progress bar and updates its status text.
     * This method simulates a background task using a `SwingWorker`. The progress bar
     * is updated from 0 to 100, with simulated delays between each step.
     * The `progressTXT` label is updated to reflect the current stage of the loading process.
     * Once the progress reaches 100%, the `loadScreen` method is called to switch to the login screen.
     */
    

    private void startProgress() {
        javax.swing.SwingWorker<Void, Integer> worker;
        worker = new javax.swing.SwingWorker<>() {
            @Override
            protected Void doInBackground() throws Exception {
                for (int i = 0; i <= 100; i++) {
                    
                    Thread.sleep(6); // Simulated delay for progress bar
                    publish(i); // Publish progress
                }
                return null;
            }

            @Override
            protected void process(java.util.List<Integer> chunks) {
                int progress = chunks.get(chunks.size() - 1);
                loadingBar.setValue(progress);
                
                // Update progressTXT based on progress
                if (progress <= 10) {
                    progressTXT.setText("Loading...");
                } else if (progress <= 30) {
                    progressTXT.setText("Loading Resources...");
                } else if (progress <= 60) {
                    progressTXT.setText("Maintaining Stocks...");
                } else if (progress <= 90) {
                    progressTXT.setText("Creating Login Environment...");
                } else {
                    progressTXT.setText("Completed!");
                }
                
            }

            @Override
            protected void done() {
                loadScreen("LoginScreen"); // Switch to login screen
            }
        };
        worker.execute(); // Start the worker thread
    }
    
    
    

